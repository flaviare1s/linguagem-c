#include <stdio.h>

int main() {
	printf("Olá, tenho 41 anos e hoje estou começando a aprender a programar em C.\n");
	
	printf("Valor inteiro: %d.\n", 10);
	printf("Valor decimal: %f.\n", 3.141516171819);
	printf("Valor decimal com apenas duas casas após a vírgula: %.2f.\n", 3.141516171819); // Coloca o número de casas decimais após %.
	printf("Imprimindo caractere: %c.\n", 'a'); // Um único caractere é cercado por aspas simples
	printf("Imprimindo texto: %s.\n", "testando"); // Strings são cercadas por aspas duplas
	
}

